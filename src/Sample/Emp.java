package Sample;
public class Emp {
	int id;
	String name;
	int salary;
	String dept;
	String address;
	public Emp(int id, String name, int salary, String dept, String address) {
		super();
		this.id = id;
		this.name = name;
		this.salary = salary;
		this.dept = dept;
		this.address = address;
	}
	public String toString() {
		return "Emp [id=" + id + ", name=" + name + ", salary=" + salary + ", dept=" + dept + ", address="
				+ address + "]";
	}
	
	
	
	

}




